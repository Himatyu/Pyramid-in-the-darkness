namespace Pyramid
{
	namespace Rendering
	{
		namespace Materials
		{
			//-----------------------------------------------------------
			// ctor
			//-----------------------------------------------------------
			PanelMaterial::PanelMaterial():
				m_Shader(this),
				m_Diffuse(1,1,1,1)
			{
			}

			//-----------------------------------------------------------
			// SetMateral
			//-----------------------------------------------------------
			void PanelMaterial::SetMateral()
			{
				m_Shader.PushDate();
				m_Shader.SetMatrix();
				m_Shader.Begin();
			}

			//-----------------------------------------------------------
			// RiseMateral
			//-----------------------------------------------------------
			void PanelMaterial::RiseMateral()
			{
				m_Shader.Eed();
			}

			//-----------------------------------------------------------
			// SetMatrix
			//-----------------------------------------------------------
			void PanelMaterial::SetMatrix(D3DXMATRIX & _mat)
			{
				m_Shader.pMatrix = &_mat;
			}
		}
	}
}

