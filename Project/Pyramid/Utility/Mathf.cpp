//-----------------------------------------------------------
// define
//-----------------------------------------------------------
namespace Utility
{
	const float	Mathf::PI = 3.14159265359;

	//-----------------------------------------------------------
	// Clamp
	//-----------------------------------------------------------
	float Mathf::Clamp(float _target, float _min, float _max)
	{
		_target = max(_target, _min);
		_target = min(_target, _max);
		return _target;
	}

	//-----------------------------------------------------------
	// Clamp01
	//-----------------------------------------------------------
	float Mathf::Clamp01(float _target)
	{
		_target = max(_target, 0.0f);
		_target = min(_target, 1.0f);
		return _target;
	}

	//-----------------------------------------------------------
	// Lerp
	//-----------------------------------------------------------
	float Mathf::Lerp(float _start, float _end, float _t)
	{
		auto dist = _end - _start;
		return (_start+dist*_t);
	}

	//-----------------------------------------------------------
	// ToRadian
	//-----------------------------------------------------------
	float Mathf::ToRadian(float _degree)
	{
		return (_degree) * (Mathf::PI / 180.0f);
	}
	//-----------------------------------------------------------
	// ToDegree
	//-----------------------------------------------------------
	float Mathf::ToDegree(float _radian)
	{
		return (_radian) * (180.0f / Mathf::PI);
	}
}

